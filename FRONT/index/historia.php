<p id="parra">La municipalidad de Mar del Plata espera inaugurar el próximo 25 de mayo la Escuela de Artes y Oficios. Las obras del nuevo edificio se estén levantando en el lugar que anteriormente ocupaba la Administración de Limpieza, en las calles 14 de Julio y Gascón. <br><br> 

En mayo de ese mismo año, la empresa Casino Pueyrredón en nota dirigida al intendente, D. José Camusso, había propuesto donar un lote de maquinarias con destino a la Sección Mecánica, además solicitaba autorización para iniciar la construcción de seis aulas en terrenos municipales. <br><br>

La escuela muestra su evolución en el aumento de la matrícula y docentes, en las distintas especialidades que se dictaron y se dictan y en los cambios de denominación a partir de su nacionalización y posterior provincialización. <br><br>

Así, por decreto N°37179/44 del Presidente Farrel pasa a depender del Ministerio de Justicia e Instrucción Pública con el nombre de Escuela Nacional de Artes y Oficios entre el 30 de diciembre de 1944 al 28 de febrero de 1945 <br><br>

Con el decreto N°6428/45 fue elevada a la categoría de Escuela Técnica hasta el 18 de abril de 1949. Al agregarse el Ciclo Técnico, pasa a denominarse Escuela Industrial de La Nación Ciclo Superior de Mar del Plata, desde el 19 de abril de 1949 hasta el 12 de junio de 1961, por resolución ministerial. <br><br>

En junio de 1961 pasó a llamarse Escuela Nacional de Educación Técnica de Mar del Plata, hasta el 17 de septiembre de 1964 y, por Resolución 1195 se le agregó al nombre de Escuela Nacional de Educación Técnica N°1, la advocación Domingo Faustino Sarmiento. <br><br>

Con la provincialización, el legendario Industrial pasó a llamarse Escuela de Educación Técnica N°3 Domingo Faustino Sarmiento y, finalmente desde el 1 de marzo de 2010 se le da el nombre de Escuela de Educación Secundaria Técnica N°3 Domingo Faustino Sarmiento. <br><br></p><br>


		
