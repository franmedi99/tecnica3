            <center><br><br><h1>Técnico en electrónica</h1></center> <br> 
<p id="parra"> Está capacitado, de acuerdo a las actividades que se desarrollan en el
perfil profesional, para: montar e instalar, operar y mantener componentes, productos, equipos e
instalaciones de electrónica analógica y/o digital; realizar proyectos, diseños y desarrollos de tecnología estándar; comercializar, seleccionar y asesorar en componentes, productos, equipos e instalaciones electrónicas; participar y/o generar emprendimientos en electrónica1<br>
.
El técnico está formado para desempeñarse en los ámbitos de Electrónica Industrial, instrumentación
electrónica, telecomunicaciones, sistemas electrónicos para computación y electrónica para la mecánica, actuando en relación de dependencia o en forma independiente en la industria electrónica, en
distintas fases de los procesos productivos de otras industrias, en empresas de telecomunicaciones, en
empresas de generación de energía eléctrica, infraestructura urbana y edilicia.
Su formación le permite actuar interdisciplinariamente con técnicos en otras áreas, eventualmente
involucrados en su actividad (procesos de transformación físico-química, construcciones civiles, mecánica, electricidad, producción agropecuaria, informática, etc.). Así mismo, prestará servicios electrónicos en forma autónoma. <br>
Como técnico es capaz de interpretar las definiciones estratégicas surgidas de los estamentos técnicos y jerárquicos correspondientes, gestionar sus actividades específicas, realizar y controlar la
totalidad de las actividades requeridas hasta su efectiva concreción, teniendo en cuenta los criterios de
seguridad, impacto ambiental, relaciones humanas, calidad, productividad y costos. <br>
El técnico desarrolla competencias comunes a todos los técnicos del sector industrial que le permiten
asumir una responsabilidad integral del proceso en el que interviene -desde su actividad específica y en
función de la experiencia acumulada- e interactuar con otros trabajadores y profesionales. <br> Estas
competencias le otorgan una base de polivalencia dentro de su ámbito ocupacional que lo preparan
para adaptarse flexiblemente a distintos roles profesionales, para trabajar interdisciplinariamente y en
equipo y para continuar aprendiendo a lo largo de toda su vida. <br>
Desarrolla también competencias específicas del campo de la electrónica que requieren del técnico el
dominio de un "saber hacer" complejo en el que se movilizan conocimientos, valores, actitudes y habilidades de carácter tecnológico, social y personal que definen su identidad profesional. </p><br>

    <center><h2>La olimpiada electrónica</h2></center> 
     
<p id="parra">En esta instancia, participarán estudiantes de Educación Técnica de la modalidad Adultos. <br>

dos duplas -conformadas por estudiantes de diferentes escuelas y jurisdicciones- deberán resolver una problemática conforme a una consigna determinada. Esas dos duplas formarán un equipo que, luego de su trabajo grupal, deberá realizar una exposición frente a sus pares y a lo/as evaluadores. <br> Además de realizar una autoevaluación, recibirán una devolución del jurado. <br>

Los trabajos expuestos recibirán menciones con carácter de Meritorios y Destacados, según criterios del grupo evaluador –consensuados federalmente- en los que se tendrán en cuenta capacidades transversales de la Tecnicatura. <br>

Para llegar a esta instancia nacional, lo/as estudiantes pasaron por una escolar/jurisdiccional -organizadas por cada jurisdicción. <br></p><br>

        
        
