ambiental, calidad y confiabilidad. <br>
•Organizar y gestionar el taller para la prestación de los servicios de mantenimiento y/o
reparaciones del sistema electrónico del automotor.
Esta función implica que el Mecánico Electrónico de Automotores, está en condiciones de organizar,
gestionar y dirigir su propio emprendimiento para la prestación de servicios de mantenimiento y/o
reparaciones de sistemas electrónicos, realizando las tareas de planificación, de comercialización de los
servicios, de supervisión del trabajo, de registro de las actividades de servicios, de gestión de personal, de
seguimiento y evaluación de los resultados físicos y económicos, de adquisición y almacenamiento de
repuestos, otros insumos y bienes de capital, y de estudio del mercado y comercialización de los servicios
profesionales. </p><br>


