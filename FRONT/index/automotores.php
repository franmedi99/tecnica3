<p id="parra">El Mecánico Electrónico de Automotores, estará formado para diagnosticar y reparar fallas en los sistemas
electrónicos del automotor – inyección de combustible, frenos, dirección, suspensión, transmisiones y
sistemas inmovilizadotes-antiarranque. Este proceso implica la medición de los componentes de estos
sistemas electrónicos – sensores y actuadores – utilizando tester, osciloscopio, o scanner. La delimitación
de la falla y la reparación del sistema requieren la comparación de las mediciones obtenidas con los
parámetros de funcionamiento definidos en el manual del fabricante y el conocimiento de las relaciones
funcionales entre los distintos componentes del sistema a reparar.
Funciones que ejerce el profesional<br>
•Gestionar el servicio y atender al cliente. <br>
En el desempeño de esta función, el Mecánico Electrónico de Automotores está en condiciones de
interpretar la información que proporciona el cliente, verificar la documentación y estado del vehículo. <br>
Además, presupuesta las tareas de reparación y/o mantenimiento luego de realizado el diagnóstico, explica
el servicio a realizar y emite la orden de trabajo.
Finalizado el servicio realiza la entrega del vehículo, documentando el trabajo efectuado e informando al
cliente de las características de las tareas ejecutadas. En todos los casos aplica normas de calidad y
confiabilidad.
•Diagnosticar fallas y reparar sistemas electrónicos de inyección de nafta.
Es una función propia del Mecánico Electrónico de Automotores, organizar y ejecutar el proceso de
diagnóstico y reparación de sistemas electrónicos de control de inyección de nafta, acondiciona el vehículo
y está capacitado para verificar el estado funcional del sistema. Asimismo, efectúa los recambios de las
partes electrónicas averiadas. En todas sus actividades aplica normas de seguridad e higiene personal y
ambiental, calidad y confiabilidad. <br>
•Diagnosticar fallas y reparar sistemas electrónicos de inyección Diésel.
Es una función propia del Mecánico Electrónico de Automotores, organizar y ejecutar el proceso de
diagnóstico y reparación de sistemas electrónicos de control de inyección Diésel, acondicionar el vehículo y
verificar el estado funcional del sistema. Asimismo, efectúa los recambios de las partes electrónicas
averiadas. En todas sus actividades aplica normas de seguridad e higiene personal y ambiental, calidad y
confiabilidad. <br>
•Diagnosticar fallas y reparar sistemas electrónicos de frenos.
Es una función propia del Mecánico Electrónico de Automotores organizar y ejecutar el proceso de
diagnóstico y reparación de sistemas electrónicos de control de frenos (ABS y Control Dinámico de
Estabilidad), acondicionar el vehículo y verificar el estado funcional del sistema. Asimismo, efectúa los
recambios de las partes electrónicas averiadas. En todas sus actividades aplica normas de seguridad e
higiene personal y ambiental, calidad y confiabilidad. <br>
