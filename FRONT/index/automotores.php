Es una función propia del Mecánico Electrónico de Automotores, organizar y ejecutar el proceso de
diagnóstico y reparación de sistemas electrónicos de control de climatización, acondicionar el vehículo y
verificar el estado funcional del sistema. Asimismo, efectúa los recambios y de las partes electrónicas
averiadas. En todas sus actividades aplica normas de seguridad e higiene personal y ambiental, calidad y
