•Realizar análisis de ensayos e interpretar sus resultados <br>
•Supervisar la ejecución de ensayos y análisis y la adecuación de los procedimientos a normas de calidad,
seguridad y manejo adecuado de residuos. <br>
•Generar y/o participar de emprendimientos vinculados con áreas de su profesionalidad <br>
•Operar y plantear mejoras en procesos químicos, físicos, fisicoquímicos y microbiológicos <br>
Cada uno de estos puntos en los ámbitos de producción, laboratorios, mantenimiento, desarrollo,
gestión y comercialización, actuando en relación de dependencia o en forma independiente. Será
capaz de interpretar las definiciones estratégicas surgidas de los estamentos técnicos y jerárquicos
pertinentes, gestionar sus actividades específicas, realizar y controlar la totalidad de las actividades
requeridas hasta su efectiva concreción, teniendo en cuenta los criterios de seguridad, impacto
ambiental, relaciones humanas, calidad y productividad. <br></p><br>


    <center><h2>La olimpiada de química en argentina</h2></center> <br><br>
    <center><h3>tiene como objetivos fundamentales:</h3></center>
<p id="parra">El Programa Olimpíada Argentina de Química constituye un conjunto de acciones promovidas por la Facultad de Ciencias Exactas y Naturales de la Universidad de Buenos Aires, cuyo fin es el de contribuir a mejorar las condiciones para la enseñanza de la Química en los niveles preuniversitarios de todo el país. Comprende, principalmente, una actividad extracurricular dirigida a alumnos regulares de Educación Media, que aborda una forma de estímulo a la sana competencia entre los mismos. <br>
Los objetivos del Programa consisten en:<br>

•Estimular la creatividad y el interés de los alumnos por la Química. <br>

•Promover un mejor conocimiento de la inserción de la Química dentro del panorama de la actividad científica y de las actividades humanas en general. <br>

•Despertar vocaciones científicas y técnicas. <br>

•Identificar a los jóvenes que muestren actitudes y talentos especiales con el fin de orientarlos y apoyarlos en sus estudios. <br>

•Contribuir directamente a la permanente capacitación y actualización de conocimientos de los docentes del sistema tradicional secundario / Polimodal / ESB. <br>

•Crear las condiciones para mejorar el nivel de la enseñanza de la Química en todas las regiones del país, destacando su vertiente experimental y ordenando los mecanismos para llegar a cada escuela y su comunidad. <br>
</p><br>



